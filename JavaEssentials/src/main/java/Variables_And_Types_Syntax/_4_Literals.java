package Variables_And_Types_Syntax;

public class _4_Literals {
    public static void main(String[] args) {
        /*
        Literals = A way to specify value inline
        int age = 25;
        * */

        //int literals
        int age = 25;
        int count = 0b101010;
        int price = 15_000;

        //double literals
        double itemPrice = 15.75;
        double coeffee = 23_456.75_24;
        double value = 5.0123E16;
        double cost = 25D; //forcing the value to double using D.

        //boolean literals
        boolean isMinor = true;
        boolean isSummer = false;

        //char literals
        char x = 'a';

        //String literals
        //In Java String is not primitive type its is an object
        String name = "Ravi";

    }
}
