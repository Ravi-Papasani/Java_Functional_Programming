package Variables_And_Types_Syntax;

public class _7_Operators {
    public static void main(String[] args) {
        //Arithmetic operators
        /*
        Additive + -
        Multiplicative * / %
        * */

        //Assignment operators
        /*
        Equals =
        += -= *= /= %= &= ^= |= <<= >>= >>>=

        int a += 5;
        int a = a + 5;
        * */

        //Postfix and Prefix operators
        /*
        expr++  expr--
        count++; (++count->prefix increment)
        count = count + 1;

        ++expr --expr
        --count
        count = count - 1;
        * */

        //Equality operators(returns always true or false)
        /*
        == !=

        boolean isCentury = val == 100;
        boolean isDoubleCentury = value != 200;
        * */

        //Relational operators
        /*
        < > <= >=
        * */

        //Logical operators
        /*
        && ||
        * */

        //Ternary operator
        /*
        ? :
        boolean bagContent = isRaining ? umbrella : hat;
        * */
    }
}
